/** 화면 설계 템플릿 빌더 — 레이아웃 행이 있으면 실제 데이터로 영역 구성 섹션을 채운다 */
export function buildScreenTemplate(
  layoutRows: Array<{ columns: Array<{ widthRatio: number; areaId?: number; label?: string }> }>,
  areas: Array<{ areaId: number; areaCode: string; name: string }>
): string {
  const header = `## 화면 개요

- **목적**:
- **접근 경로**: (예: 상단 메뉴 > 서브메뉴)

## 영역 구성

`;

  let layoutSection = "";
  if (layoutRows.length > 0) {
    layoutRows.forEach((row, i) => {
      layoutSection += `### 행 ${i + 1}\n| 영역 | 너비 |\n|------|------|\n`;
      row.columns.forEach((col) => {
        const area = col.areaId ? areas.find((a) => a.areaId === col.areaId) : null;
        const label = area ? `${area.areaCode} ${area.name}` : (col.label || "-");
        layoutSection += `| ${label} | ${col.widthRatio}% |\n`;
      });
      layoutSection += "\n";
    });
  } else {
    layoutSection = `### 행 1\n| 영역 | 너비 |\n|------|------|\n|  | 100% |\n\n`;
  }

  const footer = `## 영역 간 업무 흐름

1. [  ] 영역에서 [조회] → [  ] 영역 데이터 갱신
2. [  ] 영역에서 행 선택 → [  ] 영역 상세 표시
`;

  return header + layoutSection + footer;
}

